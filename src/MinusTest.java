public class MinusTest {
	/**
	 * @param args
	 */
	public static void main(final String[] args) {
		final Integer i = 1;
		final Long l = 2L;
		final Float f = 1.0f;
		final Double d = 2.0;

		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		System.out.println(-i);
		System.out.println(-l);
		System.out.println(-f);
		System.out.println(-d);
	}
}
